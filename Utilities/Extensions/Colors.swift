import Foundation
import UIKit

extension UIColor {
    @nonobjc class var borderGrey: UIColor {
        return UIColor(red: 230.0 / 255.0, green: 230.0 / 255.0, blue: 230.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var backgroundGrey: UIColor {
        return UIColor(red: 240.0 / 255.0, green: 240.0 / 255.0, blue: 240.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var purpleTheme: UIColor {
        return UIColor(red: 81.0 / 255.0, green: 47.0 / 255.0, blue: 127.0 / 255.0, alpha: 1.0)
    }
}
